// public java keyword is an access modifier. An access modifier allows us to define the scope or how other parts of your code or even some else's code can access this code. 
// The class keyword is used to define a class with the name following the keyword
public class Hello {

    // defining a method
    // public is an access modifier
    // static - java keyword to fin dour method to run the code we are going to add
    // void - used to indicate that the method will not return anything
    public static void main(String[] args) {
        System.out.println("Hello World");
    }
}
